// pages/page/auth/authorize/authorize.js
var common=require('../../../../utils/common.js')
var event=require('../../../../utils/event.js')
Page({

  /**
   * 页面的初始数据
   */
  data: {
    canIUse: wx.canIUse('button.open-type.getUserInfo')
  },

  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function (options) {
    // 查看是否授权
    wx.getSetting({
      success: function (res) {
        if (res.authSetting['scope.userInfo']) {
          // 已经授权，可以直接调用 getUserInfo 获取头像昵称
          wx.getUserInfo({
            success: function (res) {
              console.log("重新授权成功")
              console(res.userInfo)
            }
          })
        }
      }
    })
  },

  /**
   * 生命周期函数--监听页面初次渲染完成
   */
  onReady: function () {
  
  },

  /**
   * 生命周期函数--监听页面显示
   */
  onShow: function () {
  
  },

  /**
   * 生命周期函数--监听页面隐藏
   */
  onHide: function () {
  
  },

  /**
   * 生命周期函数--监听页面卸载
   */
  onUnload: function () {
  
  },

  /**
   * 页面相关事件处理函数--监听用户下拉动作
   */
  onPullDownRefresh: function () {
  
  },

  /**
   * 页面上拉触底事件的处理函数
   */
  onReachBottom: function () {
  
  },

  /**
   * 用户点击右上角分享
   */
  onShareAppMessage: function () {
  
  },
  bindGetUserInfo: function (e) {
    console.log(e)
    wx.setStorageSync(common.CC_HEAD_IMG, e.detail.userInfo.avatarUrl);
    wx.setStorageSync(common.CC_ENCRY_KEY, e.detail.encryptedData);
    wx.setStorageSync(common.CC_IV_KEY, e.detail.iv);
    wx.setStorageSync(common.CC_NICK_NAME, e.detail.userInfo.nickName);

    event.emit(event.KGetUserInfoSuccess, e.detail.userInfo.avatarUrl);
    wx.navigateBack();
  }

})